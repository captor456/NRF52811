/**
 * @brief 从数据中提取指定位段
 * @details 从32位数据中提取指定长度和位置的位段
 * @param[in] data   32位源数据
 * @param[in] length 要提取的位长度
 * @param[in] shift  起始位位置（从低位开始计数）
 * @return 提取的位段值
 */
static uint32_t GetBitInt(uint32_t data, uint8_t length, uint8_t shift) {
    return (data & (((1 << length) - 1) << shift)) >> shift;
}

/**
 * @brief 公历日期转整数（天数计算）
 * @details 将公历日期转换为一个整数，表示距离某个基准日的天数
 *          使用改进的Zeller公式计算，适用于1582年10月之后的日期
 * @param[in] y 年份
 * @param[in] m 月份（1-12）
 * @param[in] d 日期（1-31）
 * @return 天数整数值
 * @warning 1582年10月之前的日期计算不准确
 */
// WARNING: Dates before Oct. 1582 are inaccurate
static uint16_t SolarToInt(uint16_t y, uint8_t m, uint8_t d) {
    m = (m + 9) % 12;  /* 将月份转换为0-11，1月变为10月 */
    y = y - m / 10;    /* 调整年份，使3月为第一个月 */
    /* 计算天数：365天/年 + 闰年调整 + 月份天数 + 日期 */
    return 365 * y + y / 4 - y / 100 + y / 400 + (m * 306 + 5) / 10 + (d - 1);
}

/*============================================================================
 *                         公历转农历核心算法
 *============================================================================*/

/**
 * @brief 公历转农历核心算法
 * @details 算法流程：
 *          1. 输入验证：检查年月日是否在有效范围内
 *          2. 确定农历年份：通过solar_1_1表查找公历年份对应的农历年份
 *          3. 计算天数偏移：计算公历日期距离农历基准日的天数
 *          4. 查表计算农历月日：
 *             - 从lunar_month_days表获取该年各月大小
 *             - 逐月减去天数，得到农历月份和日期
 *          5. 处理闰月：根据闰月标志调整月份
 * 
 * @param[out] lunar       输出的农历日期结构体指针
 * @param[in]  solar_year  公历年份
 * @param[in]  solar_month 公历月份（1-12）
 * @param[in]  solar_date  公历日期（1-31）
 */
void LUNAR_SolarToLunar(struct Lunar_Date* lunar, uint16_t solar_year, uint8_t solar_month, uint8_t solar_date) {
    uint8_t i, lunarM, m, d, leap, dm;
    uint16_t year_index, lunarY, y, offset;
    uint32_t solar_data, solar11, days;

    /* 输入有效性检查 */
    if (solar_month < 1 || solar_month > 12 || solar_date < 1 || solar_date > 31 || (solar_year - solar_1_1[0] < 3) ||
        ((solar_year - solar_1_1[0]) > (sizeof(solar_1_1) / sizeof(uint32_t) - 2))) {
        /* 输入无效，返回全0 */
        lunar->Year = 0;
        lunar->Month = 0;
        lunar->Date = 0;
        lunar->IsLeap = 0;
        return;
    }

    /* 计算年份索引 */
    year_index = solar_year - solar_1_1[0];
    
    /* 将公历日期编码为32位整数，用于比较 */
    solar_data = ((uint32_t)solar_year << 9) | ((uint32_t)solar_month << 5) | ((uint32_t)solar_date);
    
    /* 如果当前公历日期小于该年1月1日对应的农历日期，则属于上一农历年 */
    if (solar_1_1[year_index] > solar_data) {
        year_index -= 1;
    }
    
    /* 获取该农历年1月1日对应的公历日期 */
    solar11 = solar_1_1[year_index];
    y = GetBitInt(solar11, 12, 9);   /* 提取年份 */
    m = GetBitInt(solar11, 4, 5);    /* 提取月份 */
    d = GetBitInt(solar11, 5, 0);    /* 提取日期 */
    
    /* 计算公历日期距离农历年首的天数偏移 */
    offset = SolarToInt(solar_year, solar_month, solar_date) - SolarToInt(y, m, d);

    /* 获取该农历年的月份数据 */
    days = lunar_month_days[year_index];
    
    /* 提取闰月月份（第13-16位） */
    leap = GetBitInt(days, 4, 13);

    /* 计算农历年份 */
    lunarY = year_index + solar_1_1[0];
    lunarM = 1;
    offset += 1;  /* 天数从1开始计数 */
    
    /* 逐月计算，确定农历月份和日期 */
    for (i = 0; i < 13; i++) {
        /* 获取第i+1月的大小：1=大月(30天)，0=小月(29天) */
        if (GetBitInt(days, 1, 12 - i) == 1) {
            dm = 30;
        } else {
            dm = 29;
        }
        
        /* 如果剩余天数大于当月天数，则进入下一个月 */
        if (offset > dm) {
            lunarM += 1;
            offset -= dm;
        } else {
            break;  /* 剩余天数即为农历日期 */
        }
    }
    
    /* 处理闰月 */
    lunar->IsLeap = 0;
    if (leap != 0 && lunarM > leap) {
        if (lunarM == leap + 1) {
            lunar->IsLeap = 1;  /* 当前月份为闰月 */
        }
        lunarM -= 1;  /* 闰月后的月份需要减1 */
    }
    
    /* 设置输出结果 */
    lunar->Month = lunarM;
    lunar->Date = offset;
    lunar->Year = lunarY;
}

