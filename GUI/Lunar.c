/** @brief 闰年和非闰年的秒数：索引0为非闰年(365天)，索引1为闰年(366天) */
uint32_t SEC_PER_YR[2] = {31536000, 31622400};

/** @brief 每月秒数表：索引0为非闰年，索引1为闰年 */
uint32_t SEC_PER_MT[2][12] = {
    {2678400, 2419200, 2678400, 2592000, 2678400, 2592000, 2678400, 2678400, 2592000, 2678400, 2592000, 2678400},
    {2678400, 2505600, 2678400, 2592000, 2678400, 2592000, 2678400, 2678400, 2592000, 2678400, 2592000, 2678400},
};

/** @brief 一天的秒数 */
#define SECOND_OF_DAY 86400

/*============================================================================
 *                         闰年判断与星期计算
 *============================================================================*/

/**
 * @brief 判断是否为闰年
 * @details 闰年规则：
 *          - 非整百年份：能被4整除的是闰年
 *          - 整百年份：能被400整除的是闰年
 * @param[in] yr 待判断的年份
 * @return 1-是闰年，0-非闰年
 * @author Denis
 */
int is_leap(int yr) {
    if (0 == (yr % 100))
        return (yr % 400 == 0) ? 1 : 0;
    else
        return (yr % 4 == 0) ? 1 : 0;
}

/**
 * @brief 根据年月日计算星期几
 * @details 使用Zeller公式的变体计算星期
 * @param[in] month 月份（1-12）
 * @param[in] day   日期（1-31）
 * @param[in] year  年份
 * @return 星期（0-6，0表示周日）
 * @author Denis
 */
unsigned char day_of_week_get(unsigned char month, unsigned char day, unsigned short year) {
    /* Month should be a number 0 to 11, Day should be a number 1 to 31 */
    static int t[] = {0, 3, 2, 5, 0, 3, 5, 1, 4, 6, 2, 4};  /* 月份偏移表 */
    year -= (uint8_t)(month < 3);  /* 1月2月视为上一年的13月14月 */
    return (year + year / 4 - year / 100 + year / 400 + t[month - 1] + day) % 7;
}

